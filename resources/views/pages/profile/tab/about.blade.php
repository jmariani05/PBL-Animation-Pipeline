<div class="mb-5">
    <h5 class="font-weight-bold text-muted">Management Pipeline Animation</h5>
    <p>
        NextGen Pipeline Animation adalah sebuah platform aplikasi berbasis web yang
        mendukung Management Animation Pipeline (proses penciptaan sebuah proyek animasi
        dari mulai pengembangan cerita sampai pada proses penyelesaian suatu film animasi).
    </p>
    <p>
        Tujuan dibuatnya aplikasi ini adalah agar pengerjaan sebuah animasi yang dilakukan
        secara kolaboratif dapat di-manage dengan baik oleh setiap kolaborator yang memiliki
        perannya masing-masing.
    </p>
</div>

<div class="mb-5">
    <h5 class="font-weight-bold text-muted">Perspektif Produk</h5>
    <p>
        Produk ini akan dijalankan oleh pengguna yang memiliki koneksi internet. Penggunaan sistem terbagi berdasarkan
        fitur-fitur yang tersedia, yaitu melihat progress pengerjaan proyek animasi, melakukan update pada status
        pengerjaan
        sebuah task, meng-upload video/scene yang telah selesai dikerjakan, dll. Produk ini dapat berjalan pada platform
        atau sistem operasi apa saja yang mendukung aplikasi berbasis web.
    </p>
</div>